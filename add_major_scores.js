function generateMajorScores(schoolName, schoolScore, schoolRank) {
    const isKeySchool = ["安徽大学", "安徽师范大学", "安徽农业大学", "安徽财经大学", "安徽医科大学", "合肥工业大学", "合肥工业大学(宣城校区)"].includes(schoolName);
    const targetCount = isKeySchool ? 15 : 10;
    
    const majors = schoolMajors[schoolName] || majorPool.slice(0, targetCount).map(m => m.name);
    const selectedMajors = majors.slice(0, Math.min(targetCount, majors.length));
    
    return selectedMajors.map((majorName, index) => {
        const major = majorPool.find(m => m.name === majorName) || { baseScore: 0 };
        const baseDiff = major.baseScore + (index % 3 - 1) * 2;
        const score2025 = Math.round(schoolScore + baseDiff);
        const rankDiff = baseDiff > 0 ? schoolRank * (baseDiff / 500) : schoolRank * (baseDiff / 400);
        const rank2025 = Math.round(schoolRank - rankDiff);
        
        return {
            majorName,
            score2025,
            rank2025,
            plan2025: 10 + (index % 5) * 8,
            score2024: Math.round(score2025 - 7 - (index % 3)),
            rank2024: Math.round(rank2025 * 1.04 + (index % 5) * 50),
            score2023: Math.round(score2025 - 12 - (index % 4)),
            rank2023: Math.round(rank2025 * 1.08 + (index % 5) * 100)
        };
    });
}

const inputPath = '/workspace/gaokao-zy/data/schools-anhui.js';
const outputPath = '/workspace/gaokao-zy/data/schools-anhui.js';

let content = fs.readFileSync(inputPath, 'utf8');

const schools = [];
let match;
const regex = /{\s*id:\s*\d+,\s*name:\s*"([^"]+)",[^}]*score:\s*(\d+),[^}]*rank:\s*(\d+)[^}]*scoreHistory:\s*\{[^}]*\}\s*\}/gs;

while ((match = regex.exec(content)) !== null) {
    schools.push({
        name: match[1],
        score: parseInt(match[2]),
        rank: parseInt(match[3]),
        fullMatch: match[0]
    });
}

console.log(`Found ${schools.length} schools`);

schools.forEach(school => {
    const majorScores = generateMajorScores(school.name, school.score, school.rank);
    const majorScoresStr = JSON.stringify(majorScores, null, 8)
        .replace(/"([^"]+)":/g, '$1:')
        .replace(/\n/g, '\n        ');
    
    const newSchoolData = school.fullMatch.replace(
        /(\n\s*scoreHistory: \{[^}]*\}\s*\n\s*\})/,
        `,\n        majorScores: ${majorScoresStr}$1`
    );
    
    content = content.replace(school.fullMatch, newSchoolData);
});

fs.writeFileSync(outputPath, content);
console.log('Done! Updated schools-anhui.js with major scores');

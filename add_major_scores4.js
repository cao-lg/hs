function generateMajorScores(schoolName, schoolScore, schoolRank) {
    const keySchools = ["安徽大学", "安徽师范大学", "安徽农业大学", "安徽财经大学", "安徽医科大学", "合肥工业大学", "合肥工业大学(宣城校区)"];
    const targetCount = keySchools.includes(schoolName) ? 15 : 10;
    
    const baseMajors = schoolMajorsConfig[schoolName] || [];
    let majors;
    if (baseMajors.length > 0) {
        majors = baseMajors.slice(0, Math.min(targetCount, baseMajors.length));
    } else {
        const liberalArts = ["汉语言文学", "法学", "英语", "新闻学", "会计学", "财务管理", "行政管理", "工商管理", "市场营销", "人力资源管理", "经济学", "金融学", "国际经济与贸易", "教育学", "学前教育", "小学教育", "历史学", "思想政治教育", "广告学", "网络与新媒体"];
        majors = liberalArts.slice(0, targetCount);
    }
    
    return majors.map((majorName, index) => {
        const major = majorPool.find(m => m.name === majorName) || { baseScore: 0 };
        const baseDiff = major.baseScore + (index % 3 - 1) * 2;
        const score2025 = Math.round(schoolScore + baseDiff);
        const rankDiff = baseDiff * 50;
        const rank2025 = Math.max(1, Math.round(schoolRank - rankDiff));
        
        return {
            majorName,
            score2025,
            rank2025,
            plan2025: 10 + (index % 5) * 8,
            score2024: Math.round(score2025 - 7 - (index % 3)),
            rank2024: Math.round(rank2025 * 1.04 + (index % 5) * 50),
            score2023: Math.round(score2025 - 12 - (index % 4)),
            rank2023: Math.round(rank2025 * 1.08 + (index % 5) * 100)
        };
    });
}

const inputPath = '/workspace/gaokao-zy/data/schools-anhui.js';
let content = fs.readFileSync(inputPath, 'utf8');

let braceCount = 0;
let inArray = false;
let currentObject = [];
let objects = [];
let objectStartIndex = -1;

const lines = content.split('\n');

for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    
    if (line.includes('[')) {
        inArray = true;
    }
    
    if (inArray) {
        const openBraces = (line.match(/\{/g) || []).length;
        const closeBraces = (line.match(/\}/g) || []).length;
        
        if (openBraces > 0 && braceCount === 0) {
            objectStartIndex = i;
        }
        
        braceCount += openBraces - closeBraces;
        
        if (braceCount === 0 && objectStartIndex >= 0 && closeBraces > 0) {
            objects.push({
                start: objectStartIndex,
                end: i,
                lines: lines.slice(objectStartIndex, i + 1)
            });
            objectStartIndex = -1;
        }
    }
}

console.log(`Found ${objects.length} objects`);

objects.reverse().forEach(obj => {
    const objText = obj.lines.join('\n');
    
    if (objText.includes('majorScores')) {
        return;
    }
    
    const nameMatch = objText.match(/name:\s*"([^"]+)"/);
    const scoreMatch = objText.match(/\n\s*score:\s*(\d+),/);
    const rankMatch = objText.match(/\n\s*rank:\s*(\d+),/);
    
    if (nameMatch && scoreMatch && rankMatch) {
        const schoolName = nameMatch[1];
        const schoolScore = parseInt(scoreMatch[1]);
        const schoolRank = parseInt(rankMatch[1]);
        
        const majorScores = generateMajorScores(schoolName, schoolScore, schoolRank);
        console.log(`Adding ${majorScores.length} majors to ${schoolName} (score: ${schoolScore})`);
        
        let majorScoresLines = JSON.stringify(majorScores, null, 8)
            .replace(/"([^"]+)":/g, '$1:')
            .split('\n')
            .map((line, i) => i === 0 ? '        majorScores: ' + line : '        ' + line);
        
        const lastLine = obj.lines[obj.lines.length - 1];
        const newLastLine = lastLine + ',';
        
        const newObjLines = [
            ...obj.lines.slice(0, -1),
            newLastLine,
            ...majorScoresLines,
            lastLine
        ];
        
        for (let i = 0; i < obj.lines.length; i++) {
            lines[obj.start + i] = newObjLines[i] || '';
        }
        
        const diff = newObjLines.length - obj.lines.length;
        objects.forEach(o => {
            if (o.start > obj.start) {
                o.start += diff;
                o.end += diff;
            }
        });
    }
});

fs.writeFileSync(inputPath, lines.join('\n'));
console.log('\nDone!');

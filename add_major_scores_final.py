def generate_major_scores(school_name, school_score, school_rank):
    key_schools = ["安徽大学", "安徽师范大学", "安徽农业大学", "安徽财经大学", "安徽医科大学", "合肥工业大学", "合肥工业大学(宣城校区)"]
    target_count = 15 if school_name in key_schools else 10
    
    base_majors = school_majors_config.get(school_name, [])
    if base_majors:
        majors = base_majors[:target_count]
    else:
        liberal_arts = ["汉语言文学", "法学", "英语", "新闻学", "会计学", "财务管理", "行政管理", "工商管理", "市场营销", "人力资源管理"]
        majors = liberal_arts[:target_count]
    
    result = []
    for i, major_name in enumerate(majors):
        major = next((m for m in major_pool if m["name"] == major_name), {"baseScore": 0})
        base_diff = major["baseScore"] + (i % 3 - 1) * 2
        score_2025 = round(school_score + base_diff)
        rank_diff = base_diff * 50
        rank_2025 = max(1, round(school_rank - rank_diff))
        
        result.append({
            "majorName": major_name,
            "score2025": score_2025,
            "rank2025": rank_2025,
            "plan2025": 10 + (i % 5) * 8,
            "score2024": round(score_2025 - 7 - (i % 3)),
            "rank2024": round(rank_2025 * 1.04 + (i % 5) * 50),
            "score2023": round(score_2025 - 12 - (i % 4)),
            "rank2023": round(rank_2025 * 1.08 + (i % 5) * 100)
        })
    
    return result

def python_to_js_obj(obj, indent=8):
    json_str = json.dumps(obj, ensure_ascii=False, indent=indent)
    json_str = re.sub(r'"(\w+)":', r'\1:', json_str)
    return json_str

input_path = '/workspace/gaokao-zy/data/schools-anhui.js'

with open(input_path, 'r', encoding='utf-8') as f:
    content = f.read()

pattern = r'(\n\s*\{\s*\n\s*id:\s*\d+,\s*\n\s*name:\s*"([^"]+)",[\s\S]*?\n\s*scoreHistory:\s*\{[\s\S]*?\n\s*\}\n\s*\})'

matches = list(re.finditer(pattern, content))
print(f'Found {len(matches)} school entries')

matches.reverse()

for match in matches:
    full_match = match.group(1)
    school_name = match.group(2)
    
    if 'majorScores' in full_match:
        continue
    
    score_match = re.search(r'\n\s*score:\s*(\d+),', full_match)
    rank_match = re.search(r'\n\s*rank:\s*(\d+),', full_match)
    
    if not score_match or not rank_match:
        continue
    
    school_score = int(score_match.group(1))
    school_rank = int(rank_match.group(1))
    
    major_scores = generate_major_scores(school_name, school_score, school_rank)
    print(f'Adding {len(major_scores)} majors to {school_name} (score: {school_score}, rank: {school_rank})')
    
    major_scores_js = python_to_js_obj(major_scores, 8)
    
    indented_lines = []
    for idx, line in enumerate(major_scores_js.split('\n')):
        if idx == 0:
            indented_lines.append('        majorScores: ' + line)
        else:
            indented_lines.append('        ' + line)
    
    major_scores_str = '\n'.join(indented_lines)
    
    def replace_func(m):
        return ',\n' + major_scores_str + m.group(0)
    
    new_entry = re.sub(
        r'(\n\s*scoreHistory:\s*\{[\s\S]*?\n\s*\}\n\s*\})',
        replace_func,
        full_match,
        count=1
    )
    
    content = content[:match.start(1)] + new_entry + content[match.end(1):]

with open(input_path, 'w', encoding='utf-8') as f:
    f.write(content)

print(f'\nDone! Updated {len(matches)} schools with major scores')

function generateMajorScores(schoolName, schoolScore, schoolRank, count = 10) {
    const majors = schoolMajors[schoolName] || majorPool.slice(0, 15).map(m => m.name);
    const selectedMajors = majors.slice(0, Math.min(count, majors.length));
    
    return selectedMajors.map((majorName, index) => {
        const major = majorPool.find(m => m.name === majorName) || { baseScore: 0 };
        const baseDiff = major.baseScore + (Math.random() - 0.5) * 6;
        const score2025 = Math.round(schoolScore + baseDiff);
        const rank2025 = Math.round(schoolRank * (1 - baseDiff / 200));
        
        return {
            majorName,
            score2025,
            rank2025,
            plan2025: Math.floor(Math.random() * 40) + 10,
            score2024: Math.round(score2025 - 8 + Math.random() * 4),
            rank2024: Math.round(rank2025 * 1.05),
            score2023: Math.round(score2025 - 13 + Math.random() * 5),
            rank2023: Math.round(rank2025 * 1.1)
        };
    });
}


function getAllSpecialPlans() {
    return {
        '国家专项': National专项Plans,
        '地方专项': Local专项Plans,
        '高校专项': College专项Plans
    };
}

function searchSpecialPlans(keyword, planType = 'all') {
    const all = getAllSpecialPlans();
    const results = [];
    
    const types = planType === 'all' ? Object.keys(all) : [planType];
    
    types.forEach(type => {
        all[type].forEach(school => {
            const schoolMatch = !keyword || 
                school.schoolName.includes(keyword) || 
                school.schoolCode.includes(keyword);
            
            if (schoolMatch) {
                results.push({ ...school, planType: type });
            } else if (keyword) {
                // 搜索专业
                const matchedGroups = [];
                school.groups.forEach(group => {
                    const matchedMajors = group.majors.filter(m => 
                        m.majorName.includes(keyword) || m.majorCode.includes(keyword)
                    );
                    if (matchedMajors.length > 0) {
                        matchedGroups.push({
                            ...group,
                            majors: matchedMajors
                        });
                    }
                });
                if (matchedGroups.length > 0) {
                    results.push({
                        ...school,
                        planType: type,
                        groups: matchedGroups
                    });
                }
            }
        });
    });
    
    return results;
}

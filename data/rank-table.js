function getRankByScore(score, subject = 'history') {
    const table = subject === 'history' ? RankTableHistory : RankTablePhysics;
    const exact = table.find(item => item.score === score);
    if (exact) return exact.cumulative;
    
    let lower = null, upper = null;
    for (let i = 0; i < table.length; i++) {
        if (table[i].score > score) {
            upper = table[i];
        } else if (table[i].score < score) {
            lower = table[i];
            break;
        }
    }
    
    if (upper && lower) {
        const ratio = (score - lower.score) / (upper.score - lower.score);
        return Math.round(lower.cumulative + ratio * (upper.cumulative - lower.cumulative));
    }
    
    return null;
}

